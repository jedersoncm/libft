#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*new;
	int		i;

	new = malloc((ft_strlen(s1) + 1) * sizeof(s1));
	if (new == NULL)
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		new[i] = s1[i];
		i++;
	}
	new[i] = '\0';
	return ((char*)new);
}
