#include <stddef.h>

void	*ft_bzero(void *str, size_t n)
{
	int		i;
	int		k;
	char	*a;

	a = str;
	i = 0;
	k = n;
	while (i < k)
	{
		a[i] = 0;
		i++;
	}
	return (str);
}
