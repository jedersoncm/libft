#include <stddef.h>

void	*ft_memset(void *str, int c, size_t n)
{
	int		i;
	int		k;
	char	*a;

	a = str;
	i = 0;
	k = n;
	while (i < k)
	{
		a[i] = c;
		i++;
	}
	return (str);
}
